"""

Print Elements of an Array

Problem: Write a function to print each element of an array on a new line using a for or while loop.

    Example:
        Input: [1, 2, 3, 4]
        Output:
            1
            2
            3
            4

Solution:
    Loop through array and print it

"""

def print_element(arr):
    for i in arr:
        print(i)


print_element([1,2,3,4])
